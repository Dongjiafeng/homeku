package yesterday.one;

public class Teacher {
    //教学编号
    private  int tId;
     private  String gender;
     private  int age;

    public Teacher() {
    }

    public int getTId() {
        return tId;
    }

    public void setTld(int tld) {
        this.tId = tId;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
