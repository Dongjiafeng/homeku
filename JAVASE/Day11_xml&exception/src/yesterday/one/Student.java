package yesterday.one;

public class Student {
    //学号
    private int sId;
    private  String gender;
    private  int age;

    public Student() {

    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getsId() {
        return sId;
    }

    public void setsID(int sID) {
        this.sId = sId;
    }
}
